package com.mindsprint.project1.basic;

public class Assignment {
    public static void main(String[] args) {

        int num1 = 10;
        System.out.println("Original value: " +num1);

        num1 += 2;
        System.out.println("Incremented value: " +num1);


        num1 -= 2;
        System.out.println("After Decrementing value: " +num1);
    }
}
