package com.mindsprint.project1.basic;

public class Conditional {
    public static void main(String[] args) {

        int num1 = 10;
        int num2 = 20;

        // if we add the scanner input and output we do not get warning
        System.out.println("num1 > num2: " + (num1 > num2));
        System.out.println("num1 > num2: " + (num1 < num2));
        System.out.println("num1 > num2: " + (num1 == num2));
        System.out.println("num1 > num2: " + (num1 >= num2));
        System.out.println("num1 > num2: " + (num1 <= num2));
        System.out.println("num1 > num2: " + (num1 != num2));
    }
}
